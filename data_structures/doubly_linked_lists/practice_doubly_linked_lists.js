  push(value) {}
  // REMOVE NODE FROM END OF DOUBLY LINKED LIST AND RETURN NODE
  pop() {}
  //   ADD NODE TO BEGINNING OF DOUBLY LINKED LIST
  unshift(value) {}
  // REMOVE NODE FROM BEGINNING OF DOUBLY LINKED LIST AND RETURN NODE
